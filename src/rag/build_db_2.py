def process_movie_corpus(file_path, limit=None):
    """Loads pre-chunked movie JSONL dataset and safely formats metadata."""
    print(f"Loading movie corpus from {file_path}...")
    docs, metadatas, ids = [], [], []

    if not os.path.exists(file_path):
        print(f"Warning: File not found at {file_path}.")
        return docs, metadatas, ids

    with open(file_path, "r", encoding="utf-8") as f:
        for i, line in enumerate(f):
            if limit and i >= limit:
                break

            row = json.loads(line)

            docs.append(row["text"])

            meta_dict = {
                "source": row.get("source", "unknown"),
                "title": str(row.get("title", ""))[:100]
            }

            for k, v in row.get("meta", {}).items():
                meta_dict[k] = str(v)

            metadatas.append(meta_dict)

            prefix = row.get("source", "doc").replace("/", "_").replace(" ", "")
            chunk_id = row.get("chunk_id", f"chk_{i}")
            ids.append(f"{prefix}_{chunk_id}")

    return docs, metadatas, ids

def build_movie_vector_store(testing_mode=False):
    print("Initializing ChromaDB client for Movie Domain...")
    client = chromadb.PersistentClient(path=DB_PATH)

    sentence_transformer_ef = embedding_functions.SentenceTransformerEmbeddingFunction(
        model_name="all-MiniLM-L6-v2"
    )

    # Creating collection for movies
    collection = client.get_or_create_collection(
        name="movie_knowledge_base",
        embedding_function=sentence_transformer_ef,
        metadata={"hnsw:space": "cosine"}
    )

    jsonl_limit = 500 if testing_mode else None

    # Load Movie Database
    all_docs, all_metadatas, all_ids = process_movie_corpus(MOVIE_PATH, limit=jsonl_limit)

    batch_size = 5000
    total_docs = len(all_docs)
    print(f"\nTotal movie documents ready for ingestion: {total_docs}")

    if total_docs == 0:
        print("No documents loaded!")
        return

    print("Embedding and adding to ChromaDB...")

    for idx in tqdm(range(0, total_docs, batch_size), desc="Ingesting Movie Batches"):
        end_idx = min(idx + batch_size, total_docs)
        collection.add(
            documents=all_docs[idx:end_idx],
            metadatas=all_metadatas[idx:end_idx],
            ids=all_ids[idx:end_idx]
        )

    print(f"\nSuccess! Full offline movie vector database compiled at {DB_PATH}")

