import os
import json
import chromadb
from chromadb.utils import embedding_functions
from tqdm import tqdm

def create_politics_vector_store(
    jsonl_path="politics_rag/corpus_chunked.jsonl",
    persist_directory="politics_vector_store"
):
    """
    Reads the chunked Politics corpus (Miller Center & Europarl)
    and builds a ChromaDB vector store.
    """

    if not os.path.exists(jsonl_path):
        raise FileNotFoundError(f"Cannot find {jsonl_path}. Did Wilbert's cell finish running?")

    print(f"Initializing ChromaDB persistent client at ./{persist_directory}")
    client = chromadb.PersistentClient(path=persist_directory)

    # Initialize the embedding function
    print("Loading all-MiniLM-L6-v2 embedding function...")
    sentence_transformer_ef = embedding_functions.SentenceTransformerEmbeddingFunction(
        model_name="all-MiniLM-L6-v2"
    )

    # Create the collection
    collection = client.get_or_create_collection(
        name="politics_knowledge_base",
        embedding_function=sentence_transformer_ef,
        metadata={"description": "CrossNER Politics subset RAG corpus (Miller Center & Europarl)"}
    )

    documents = []
    metadatas = []
    ids = []

    print("Reading JSONL records and preparing metadata...")
    with open(jsonl_path, 'r', encoding='utf-8') as f:
        for line in f:
            record = json.loads(line)

            chunk_id = record["chunk_id"]
            text = record["text"]

            # Flatten metadata for ChromaDB compatibility
            meta = record.get("meta", {})
            flat_meta = {
                "source": record.get("source", "Unknown"),
                "title": record.get("title", "Untitled")
            }

            # Miller Center specific metadata
            if "president" in meta:
                flat_meta["president"] = str(meta["president"])
            if "date" in meta:
                flat_meta["date"] = str(meta["date"])
            if "url" in meta:
                flat_meta["url"] = str(meta["url"])

            # Europarl specific metadata
            if "file" in meta:
                flat_meta["file"] = str(meta["file"])

            documents.append(text)
            metadatas.append(flat_meta)
            ids.append(chunk_id)

    total_chunks = len(ids)
    print(f"Found {total_chunks} chunks. Beginning embedding and database insertion...")

    batch_size = 5461

    for i in tqdm(range(0, total_chunks, batch_size), desc="Uploading to Chroma"):
        end_idx = min(i + batch_size, total_chunks)
        collection.add(
            documents=documents[i:end_idx],
            metadatas=metadatas[i:end_idx],
            ids=ids[i:end_idx]
        )

    print(f"\nSuccess! Politics Vector Store built at ./{persist_directory}")

if __name__ == "__main__":
    create_politics_vector_store()

