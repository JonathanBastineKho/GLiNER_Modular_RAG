import os
import json
import chromadb
from chromadb.utils import embedding_functions
from tqdm import tqdm

def create_ai_vector_store(
    jsonl_path="ai_rag/ai_rag_corpus_chunked.jsonl",
    persist_directory="ai_vector_store"
):
    """
    Reads the chunked AI corpus and builds a ChromaDB vector store
    using the all-MiniLM-L6-v2 embedding model.
    """

    if not os.path.exists(jsonl_path):
        raise FileNotFoundError(f"Cannot find {jsonl_path}. Did Wilbert's script finish running?")

    print(f"Initializing ChromaDB persistent client at ./{persist_directory}")
    client = chromadb.PersistentClient(path=persist_directory)

    print("Loading all-MiniLM-L6-v2 embedding function...")
    sentence_transformer_ef = embedding_functions.SentenceTransformerEmbeddingFunction(
        model_name="all-MiniLM-L6-v2"
    )

    # Create or get the collection
    collection = client.get_or_create_collection(
        name="ai_knowledge_base",
        embedding_function=sentence_transformer_ef,
        metadata={"description": "CrossNER AI subset RAG corpus (arXiv & DBLP)"}
    )

    documents = []
    metadatas = []
    ids = []

    print("Reading and flattening JSONL records...")
    with open(jsonl_path, 'r', encoding='utf-8') as f:
        for line in f:
            record = json.loads(line)

            chunk_id = record["chunk_id"]
            text = record["text"]

            # Flatten metadata for ChromaDB compatibility
            meta = record.get("meta", {})
            flat_meta = {
                "source": record.get("source", "Unknown"),
                "title": record.get("title", "Untitled")
            }

            # Convert lists to comma-separated strings
            if "authors" in meta and isinstance(meta["authors"], list):
                flat_meta["authors"] = ", ".join(meta["authors"])
            if "categories" in meta and isinstance(meta["categories"], list):
                flat_meta["categories"] = ", ".join(meta["categories"])

            # Grab DBLP specific meta if it exists
            if "venue" in meta:
                flat_meta["venue"] = str(meta["venue"])
            if "year" in meta:
                flat_meta["year"] = str(meta["year"])
            if "type" in meta:
                flat_meta["type"] = str(meta["type"])

            # Grab arXiv specific meta if it exists
            if "arxiv_id" in meta:
                flat_meta["arxiv_id"] = str(meta["arxiv_id"])

            documents.append(text)
            metadatas.append(flat_meta)
            ids.append(chunk_id)

    total_chunks = len(ids)
    print(f"Found {total_chunks} chunks. Beginning embedding and database insertion...")


    batch_size = 5461

    for i in tqdm(range(0, total_chunks, batch_size), desc="Uploading to Chroma"):
        end_idx = min(i + batch_size, total_chunks)
        collection.add(
            documents=documents[i:end_idx],
            metadatas=metadatas[i:end_idx],
            ids=ids[i:end_idx]
        )

    print(f"\nSuccess! AI Vector Store built at ./{persist_directory}")

if __name__ == "__main__":
    create_ai_vector_store()

